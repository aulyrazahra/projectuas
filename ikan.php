			<h2 class="wp-block-heading"> <strong> 2. Ikan kembung sambalado </strong> </h2> 
                <br><img src="images/ikanbalado.jpg" width="1000px" height="500px"> 
					<p><strong>Bahan-bahan:</strong></p>
					<ul>
					<li>6 ekor ikan kembung</li>
